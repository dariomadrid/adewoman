// Helper functions
export function getPortfolioItem(id) {
  return portfolioItems.find(item => item.id === id.toString());
}

export function getAllPortfolioItems() {
  return portfolioItems;
}

export function getPortfolioItemsForDisplay() {
  // Return with reactive properties for Vue component
  return portfolioItems.map(item => ({
    ...item,
    imageLoaded: false,
    isVisible: false,
    isHovered: false
  }));
}